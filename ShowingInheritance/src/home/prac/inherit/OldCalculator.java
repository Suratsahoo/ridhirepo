package home.prac.inherit;

public class OldCalculator {

	public int a,b;
	public void set(int a, int b)
	{
		this.a=a;
		this.b=b;
	}
	public int addition()
	{
		return a+b;
	}
	public int substraction()
	{
		return a-b;
	}
	public double division()
	{
		return a/b;
	}
	public int multiplication()
	{
		return a*b;
	}
}
